package restauranteApp.model.domain;

public class Bebida extends Produtos {
	
	private String tipo;
	private double volume;
	private int calorias;
	
	public Bebida(String idProduto, String nome, double preco, String categoria, String tipo, double volume, int calorias) {
		super(idProduto, nome, preco, categoria);
		this.tipo = tipo;
        this.volume = volume;
        this.calorias = calorias;
	}
	
	@Override
	 public String toString() {
     return "Bebida -> tipo = " + tipo + ", volume = " + volume + "calorias = " + calorias;
	 }

	public String getTipo() {
		return tipo;
	}

	public void setTipo(String tipo) {
		this.tipo = tipo;
	}

	public double getVolume() {
		return volume;
	}

	public void setVolume(double volume) {
		this.volume = volume;
	}

	public int getCalorias() {
		return calorias;
	}

	public void setCalorias(int calorias) {
		this.calorias = calorias;
	}
	
	
}
